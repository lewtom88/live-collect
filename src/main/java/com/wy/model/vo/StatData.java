package com.wy.model.vo;

public class StatData {

    private String comment;
    private String game;
    private String bonus;
    private String kda;
    private String watchLike;
    private String gift;
    private String giftHistory;
    private String topComment;

    public String getComment() {
        return comment;
    }

    public void setComment(String comment) {
        this.comment = comment;
    }

    public String getGame() {
        return game;
    }

    public void setGame(String game) {
        this.game = game;
    }

    public String getBonus() {
        return bonus;
    }

    public void setBonus(String bonus) {
        this.bonus = bonus;
    }

    public String getKda() {
        return kda;
    }

    public void setKda(String kda) {
        this.kda = kda;
    }

    public String getWatchLike() {
        return watchLike;
    }

    public void setWatchLike(String watchLike) {
        this.watchLike = watchLike;
    }

    public String getGift() {
        return gift;
    }

    public void setGift(String gift) {
        this.gift = gift;
    }

    public String getGiftHistory() {
        return giftHistory;
    }

    public void setGiftHistory(String giftHistory) {
        this.giftHistory = giftHistory;
    }

    public String getTopComment() {
        return topComment;
    }

    public void setTopComment(String topComment) {
        this.topComment = topComment;
    }
}
